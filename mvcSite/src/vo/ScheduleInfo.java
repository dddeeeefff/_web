package vo;

public class ScheduleInfo {
	private int si_idx;
	private String mi_id, si_start, si_end, si_content, si_date;
	
	public int getSi_idx() {
		return si_idx;
	}
	public void setSi_idx(int si_idx) {
		this.si_idx = si_idx;
	}
	public String getMi_id() {
		return mi_id;
	}
	public void setMi_id(String mi_id) {
		this.mi_id = mi_id;
	}
	public String getSi_start() {
		return si_start;
	}
	public void setSi_start(String si_start) {
		this.si_start = si_start;
	}
	public String getSi_end() {
		return si_end;
	}
	public void setSi_end(String si_end) {
		this.si_end = si_end;
	}
	public String getSi_content() {
		return si_content;
	}
	public void setSi_content(String si_content) {
		this.si_content = si_content;
	}
	public String getSi_date() {
		return si_date;
	}
	public void setSi_date(String si_date) {
		this.si_date = si_date;
	}

}
